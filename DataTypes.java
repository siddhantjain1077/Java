public class DataTypes{
    public static void main(String args[]){
        char a = 'J';
        int b = 40;
        float c = 56.764567f;
        byte d = 78;
        String e = "Hello World";
        short f = 990;
        long g = 76045980;

        System.out.println("Character a = " +a);
        System.out.println("Integer b = " +b);
        System.out.println("Float c = " +c);
        System.out.println("Byte d = " +d);
        System.out.println("String e = " +e);
        System.out.println("Short f = " +f);
        System.out.println("Long g = " +g);
    }
}